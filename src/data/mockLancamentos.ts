const WHP_LANCAMENTOS_KEY = 'whp_lancamentos';

export function loadLancamentos(): Lancamento[] {
  const stored = localStorage.getItem(WHP_LANCAMENTOS_KEY);
  if (stored) {
    try {
      return JSON.parse(stored);
    } catch {
      // fall through
    }
  }
  localStorage.setItem(WHP_LANCAMENTOS_KEY, JSON.stringify(SEED_LANCAMENTOS));
  return [...SEED_LANCAMENTOS];
}

export function saveLancamentos(lancamentos: Lancamento[]): void {
  localStorage.setItem(WHP_LANCAMENTOS_KEY, JSON.stringify(lancamentos));
}
