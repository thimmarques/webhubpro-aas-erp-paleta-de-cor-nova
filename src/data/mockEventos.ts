const WHP_EVENTOS_KEY = 'whp_eventos';

export function getEventos(): Evento[] {
  const stored = localStorage.getItem(WHP_EVENTOS_KEY);
  if (stored) {
    try {
      return JSON.parse(stored);
    } catch {
      // fall through
    }
  }
  localStorage.setItem(WHP_EVENTOS_KEY, JSON.stringify(SEED_EVENTOS));
  return [...SEED_EVENTOS];
}

export function saveEventos(eventos: Evento[]): void {
  localStorage.setItem(WHP_EVENTOS_KEY, JSON.stringify(eventos));
}
